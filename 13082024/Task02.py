# Performing max, power, subtraction, multiplication, addition and division operation.

num1 = int(input("Enter the num1 : "))
num2 = int(input("Enter the num2 : "))
print(f"The maximum number between {num1} and {num2} is", max(num1, num2))
print(f"The Power of {num1} and {num2} is", pow(num1, num2))
sub = num1 - num2
print(f"The subtraction value of {num1} and {num2} is", sub)
mul = num1 * num2
print(f"The multiplication value of {num1} and {num2} is", mul)
add = num1 + num2
print(f"The addition value of {num1} and {num2} is", add)
div = num1/num2
print(f"The division value of {num1} and {num2} is", div)
